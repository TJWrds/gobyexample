package main

import "fmt"

func main() {

	var a = "string"
	fmt.Println(a)
	a = "new string value"
	fmt.Println(a)
	a = "1"
	fmt.Println(a)

	var b, c int = 1, 2
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	var e int
	fmt.Println(e)

	var ef uint
	ef = 100
	fmt.Println(ef)

	f := "apple"
	fmt.Println(f)

}
